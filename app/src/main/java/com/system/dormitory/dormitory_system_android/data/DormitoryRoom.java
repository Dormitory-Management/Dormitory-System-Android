public class DormitoryRoom {
    private int room;
    private int count;

    public DormitoryRoom() {

    }

    public DormitoryRoom(int room) {
        this.room = room;
        this.count = count;
    }

    public int getRoom() {
        return room;
    }

    public void setRoom(int room) {
        this.room = room;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
